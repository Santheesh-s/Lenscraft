<h2><font color="FireBrick">Dutch Angle or Dutch Tilt Shot</font></h2>
<p>For a Dutch angle (Dutch tilt), the camera is slanted to one side. <br>With the horizon lines tilted in this way, you can create a sense of disorientation, a de-stabilized mental state, or increase the tension.</p>
<p>Dutch angle camera angle focusing on the precise moment to use one. <br>Watch as the scene begins with "level" and "normal" shots before switching to Dutch angles for maximum impact.</p>
<p>The Dutch angle, or Dutch tilt, is a camera technique that dates back to German films of the late 1920s before it started finding wider acceptance in Hollywood in the 1930s. Characterized by a tilted X-axis, dutch angles are used by film-makers to create a dramatic effect and leave the audience feeling confused, unstable and disoriented. 

